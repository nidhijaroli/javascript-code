// meter to foot 

function meterToFoot(meter) {
    return meter * 3.281;
  }
  

  let meter = 5;
  console.log("Meter to Foot:", meterToFoot(meter));
  
  
  // Celsius to Fahrenheit

  function celsiusToFahrenheit(celsius) {
    return (celsius * 9/5) + 32;
  }
  
  let celsius = 25;
  console.log("Celsius to Fahrenheit:", celsiusToFahrenheit(celsius));

  // Dollar to Rupees

  function dollarToRupees(dollar) {
    // Assuming 1 Dollar = 75 Rupees
    return dollar * 75;
  }
  
  let dollar = 10;
  console.log("Dollar to Rupees:", dollarToRupees(dollar));
  
  